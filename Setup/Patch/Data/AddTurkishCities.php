    {
        $provinces = array_values($this->getProvinces($cityCode, $regionId));
        foreach ($provinces as $province) {
            $bind = [
                'region_id' => $regionId,
                'city' => $province['name'],
                'district_id' => $province['districtID'],
                'region_code' => $province['cityCode']
            ];
            $this->moduleDataSetup->getConnection()
                ->insert($this->moduleDataSetup->getTable('magedesk_trcity_city'), $bind);
        }
    }

