    /**
     * Convert Turkish characters to English
     *
     * @param string $text
     * @return string
     */
    public function tr2en($text)
    {

        $trans=[
            "Ç" => "C",
            "Ğ" => "G",
            "İ" => "I",
            "Ö" => "O",
            "Ş" => "S",
            "Ü" => "U",
            "ç" => "c",
            "ğ" => "g",
            "ı" => "i",
            "ö" => "o",
            "ş" => "s",
            "ü" => "u",
        ];

        return strtr($text, $trans);
    }

